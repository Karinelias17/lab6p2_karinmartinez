        ArrayList <Usuarios> miembros = new ArrayList();
        ArrayList <Pokegrupo> pokegrupo = new ArrayList();
        Date creacion= new Date();
        ArrayList <Usuarios> usuarios =new ArrayList();
        usuarios = f.getUsuarios();
        nombre = txtnombre.getText();
        lider = f.getIngresado();
        tipo = "novato";
        int pos=0;
        for (Usuarios usuario2 : usuarios){
            if (usuario2.getUsuario().equals(f.getIngresado())){
                miembros.add(usuarios.get(pos));
            }
        } 
        pokegrupo.add(new Pokegrupo(nombre,lider,tipo,miembros,creacion));
        f.setPokegrupos(pokegrupo);
        
        ArrayList <Pokegrupo> pokegrupos = new ArrayList(); 
